    # Порог схожести (0.0 - 1.0). 0.60 = 60% совпадения символов
    SIMILARITY_THRESHOLD = 0.60

    def __init__(self):
        # Построить обратный индекс: вариант -> каноническое слово
        self.variant_to_canonical = {}
        for canonical, variants in self.KEYWORDS.items():
            for variant in variants:
                self.variant_to_canonical[variant.lower()] = canonical

    def similarity(self, a: str, b: str) -> float:
        """Расстояние Левенштейна в виде коэффициента схожести (0-1)"""
        return SequenceMatcher(None, a.lower(), b.lower()).ratio()

    def find_best_match(self, word: str) -> tuple:
        """
        Находит лучшее совпадение для слова.
        Returns: (canonical_word, similarity_score) или (None, 0)
        """
        word_lower = word.lower().strip(".,!?;:-'")

        # Точное совпадение
        if word_lower in self.variant_to_canonical:
            return self.variant_to_canonical[word_lower], 1.0

        # Fuzzy matching по всем вариантам
        best_match = None
        best_score = 0.0

        for variant, canonical in self.variant_to_canonical.items():
            score = self.similarity(word_lower, variant)
            if score > best_score and score >= self.SIMILARITY_THRESHOLD:
                best_score = score
                best_match = canonical

        return best_match, best_score

    def correct_text(self, text: str, log_corrections: bool = True) -> str:
        """
        Исправляет текст, заменяя похожие слова на канонические.
        Сохраняет регистр и пунктуацию.
        """
        if not text:
            return text

        # Разбиваем на токены: слова и пунктуация
        # Используем regex, который сохраняет пробелы и пунктуацию
        tokens = re.findall(r"[\w\-'А-Яа-яёЁ]+|[^\w\-'А-Яа-яёЁ]+", text)
        corrected = []
        corrections_made = []

        for token in tokens:
            # Пропускаем пробелы и пунктуацию
            if not re.match(r"[\w\-'А-Яа-яёЁ]+", token):
                corrected.append(token)
                continue

            # Пропускаем короткие слова (менее 3 символов)
            clean_word = token.strip(".,!?;:-'")
            if len(clean_word) <= 2:
                corrected.append(token)
                continue

            canonical, score = self.find_best_match(clean_word)

            if canonical and score < 1.0:  # Неточное совпадение — исправляем
                # Сохраняем регистр первой буквы
                if token[0].isupper():
                    canonical = canonical[0].upper() + canonical[1:]
                # Сохраняем оригинальную пунктуацию в конце слова
                trailing_punct = ""
                for char in reversed(token):
                    if char in ".,!?;:-': ":
                        trailing_punct = char + trailing_punct
                    else:
                        break
                corrected.append(canonical + trailing_punct)
                corrections_made.append(f"'{token}' → '{canonical + trailing_punct}' ({score:.0%})")
            else:
                corrected.append(token)

        if corrections_made and log_corrections:
            import logging
            logger = logging.getLogger(__name__)
            logger.info(f"🎯 Fuzzy corrections: {' | '.join(corrections_made)}")

        return "".join(corrected)


